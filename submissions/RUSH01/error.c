void	error(argc, argv)
{
	int	i;

	if (argc != 17)
	{
		write(2, &"Error", 6);
		return (0);
	}
	else
	{
		i = 0;
		while (argv[i] != '\0')
		{
			if (argv[i] >= '0' || argv[i] <= '9')
			{
				write(2, &"Error", 6);
				return (0);
			}
			else
				i++;
		}
	}
}
