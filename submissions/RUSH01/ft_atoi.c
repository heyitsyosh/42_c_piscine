int	ft_atoi(char *str)
{
	int	i;
	int	nbr[16];

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= '0') && (str[i] <= '9'))
		{
			nbr[i] = str[i] - '0';
			i++;
		}
		else if (str[i] == ' ')
			i++;
		return (nbr);
	}
}