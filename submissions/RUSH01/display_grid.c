void display_grid(int arr[4][4])
{
	int i;
	int j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			ft_putstr(arr);
			write(1, &" ", 1);
			j++;
		}
		write(1, &"\n", 2);
		i++;
	}
}